
public class CalcCls {

	int number1;
	int number2;
	int result;
	
	void add() {
		result=number1+number2;
		System.out.println("Result= "+result);
	}
	void sub() {
		result=number1-number2;
		System.out.println("Result= "+result);
	}
	void mul() {
		result=number1*number2;
		System.out.println("Result= "+result);
	}
	void div() {
		result=number1/number2;
		System.out.println("Result= "+result);
	}

}
